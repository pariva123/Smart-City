    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String doc_id = jTextField_doc.getText();
        String name = jTextField_name.getText();
        String qualification = jTextField_quali.getText();
        String mobile_no = jTextField_mobile.getText();
        String email_id = jTextField_email.getText();
        
        PreparedStatement ps;
        String query = " INSERT INTO `health`( `doc_id`, `name`, `qualification`, `mobile_no`,`email_id`) VALUES (?,?,?,?,?)";
        try {
            ps =  MyConnection.getConnection().prepareStatement(query);
            ps.setString(1,doc_id);
            ps.setString(2,name);
            ps.setString(3,qualification);
            ps.setString(4,mobile_no);
            ps.setString(5,email_id);
             
       
            
            
            if(ps.executeUpdate()>0)
            {
                JOptionPane.showMessageDialog(null," Submitted");
            } 
        } catch (SQLException ex) {
            Logger.getLogger(RegsiterForms.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1MouseClicked

