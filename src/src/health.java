    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        PreparedStatement ps;
        String doc_id = jTextField_doc.getText();
        try{
            ps = MyConnection.getConnection().prepareStatement("select * from health where doc_id = ?");
            ps.setString(1,doc_id);
            rs =ps.executeQuery();
            if(rs.next()==true){
                jTextField_name.setText(rs.getString(2));
                jTextField_quali.setText(rs.getString(3));
                jTextField_mobile.setText(rs.getString(4));
                jTextField_email.setText(rs.getString(5));
            }
            
            }
        catch (SQLException ex){
        Logger.getLogger(Add_Atm.class.getName()).log(Level.SEVERE,null,ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

